private void populateTable() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    jTable1.setDefaultRenderer(Object.class, new instructorman.AlternateRowRenderer());
    String[] columnNames = {"Instructor Name", "ID", "Department", "Telephone Number"};
    model.setColumnIdentifiers(columnNames);

    for (int s = 0; s < arrays.instructors.length; s++) {
        if (arrays.instructors[s] != null) {
            List<String> instructors = new ArrayList<>();

            for (int m = 0; m < arrays.instructors[s].getDepartments().size(); m++) {
                department Departments = arrays.instructors[s].getDepartments().get(m);
                instructors.add(Departments.getName());
            }

            // Convert the ArrayLists to comma-separated strings
            String depset = String.join(", ", instructors);

            Object[] rowData = {
                "  -  " + arrays.instructors[s].getFirstname() + " " + arrays.instructors[s].getLastname(),
                "   -  " + arrays.instructors[s].getId(),
                " -  " + depset,
                " -  " + arrays.instructors[s].getTelephone()
            };
            model.addRow(rowData);
        }
    }

    
}
