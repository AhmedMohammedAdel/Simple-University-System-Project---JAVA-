    private void jLabel16MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel16MouseClicked
   try {
            int idcor = Integer.parseInt(nameadd4.getText());
            int idpre = Integer.parseInt(nameadd5.getText());
            int indexcor = -1;
            int indexpre = -1;

            for (int i = 0; i < arrays.courses.length; i++) {
                if (arrays.courses[i] != null && arrays.getCourses()[i].getCoursecode() == idcor) {
                    indexcor = i;
                    break;
                }
            }

            for (int s = 0; i < arrays.courses.length; s++) {
                if (arrays.courses[i] != null && arrays.courses[s].getCoursecode() == idpre) {
                    indexpre = s;
                    break;
                }
            }

            if (indexpre != -1 && indexcor != -1) {
                ArrayList<course> Courses = arrays.getCourses()[indexcor].getPrerequist() ;
                Courses.add(arrays.courses[indexpre]);
                arrays.getCourses()[indexcor].setPrerequist((ArrayList<course>) Courses);
                JOptionPane.showMessageDialog(null, "Course has been Added successfully. Refresh the table to check.", "Important Note", HEIGHT, cor);

                populateTable(); // Refresh the table with updated data
            } else {
                JOptionPane.showMessageDialog(null, "Sorry, an Error Occurred!", "Error", JOptionPane.ERROR_MESSAGE);
            }
       } catch (java.lang.NumberFormatException ssss) {
          JOptionPane.showMessageDialog(null, "Sorry, an Error Occurred!", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (java.lang.NullPointerException asdss ) {
         JOptionPane.showMessageDialog(null, "Sorry, an Error Occurred!", "Error", JOptionPane.ERROR_MESSAGE);

     }
        
    }//GEN-LAST:event_jLabel16MouseClicked

