private void populateTable() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    jTable1.setDefaultRenderer(Object.class, new Courcesman.AlternateRowRenderer());
    String[] columnNames = {"Course Name", "ID", "Credit", " prerequist Courses "};
    model.setColumnIdentifiers(columnNames);

    for (int s = 0; s < arrays.courses.length; s++) {
        if (arrays.courses[s] != null) {
            List<String> courseprereq = new ArrayList<>();

            for (int m = 0; m < arrays.courses[s].getPrerequist().size(); m++) {
                course Course = arrays.courses[s].getPrerequist().get(m);
                courseprereq.add(Course.getCoursename());
            }

            // Convert the ArrayLists to comma-separated strings
            String prereq = String.join(", ", courseprereq);

            Object[] rowData = {
                "  -  " + arrays.getCourses()[s].getCoursename(),
                "   -  " + arrays.getCourses()[s].getCourseCode(),
                " -  " + arrays.getCourses()[s].getCredit(),
                " -  " + prereq
            };
            model.addRow(rowData);
        }
    }
}


