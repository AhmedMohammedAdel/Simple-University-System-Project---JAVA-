private void populateTable() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    jTable1.setDefaultRenderer(Object.class, new manDepartments.AlternateRowRenderer());
    String[] columnNames = {"Department Name", "ID", "Faculties", "Courses"};
    model.setColumnIdentifiers(columnNames);

    for (int s = 0; s < arrays.dep.length; s++) {
        if (arrays.dep[s] != null) {
            List<String> facul = new ArrayList<>();
            List<String> courseList = new ArrayList<>();

            for (int m = 0; m < arrays.dep[s].getFacultys().size(); m++) {
                faculty faculties = arrays.dep[s].getFacultys().get(m);
                facul.add(faculties.getName());
            }

            for (int b = 0; b < arrays.dep[s].getCourses().size(); b++) {
                course courses = arrays.dep[s].getCourses().get(b);
                courseList.add(courses.getCoursename());
            }

            // Convert the ArrayLists to comma-separated strings
            String facultiesString = String.join(", ", facul);
            String courseString = String.join(", ", courseList);

            Object[] rowData = {
                "  -  " + arrays.dep[s].getName(),
                "   -  " + arrays.dep[s].getId(),
                " -  " + facultiesString,
                " -  " + courseString
            };
            model.addRow(rowData);
        }
    }

}
