    private void jLabel20MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel20MouseClicked
        try {
            if (idfac2.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Please Enter the ID of Faculty!", "Important Note", HEIGHT, warn);
            } else if (iddep4.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Please Enter the ID of Department!", "Important Note", HEIGHT, warn);
            } else {
                int corId = Integer.parseInt(idfac2.getText());
                int departmentId = Integer.parseInt(iddep4.getText());
                int departmentIndex = -1;
                int corIndex = -1;

                // Find the department index
                for (int i = 0; i < arrays.dep.length; i++) {
                    if (arrays.dep[i] != null && arrays.dep[i].getId() == departmentId) {
                        departmentIndex = i;
                        break;
                    }
                }

                // Find the faculty index within the department
                if (departmentIndex != -1) {
                    List<course> courses = arrays.dep[departmentIndex].getCourses() ;
                    for (int i = 0; i < courses.size(); i++) {
                        if (courses.get(i).getCourseCode() == corId) {
                            corIndex = i;
                            break;
                        }
                    }
                }

                // Delete the faculty if found
                if (departmentIndex != -1 && corIndex != -1) {
                    List<course> courses = arrays.dep[departmentIndex].getCourses() ;
                    courses.remove(corIndex);
                    arrays.dep[departmentIndex].setCourses((ArrayList<course>) courses);
                    populateTable(); // Refresh the table with updated data
                    JOptionPane.showMessageDialog(null, "Course has been deleted successfully. Refresh the table to check.", "Important Note", HEIGHT, cor);
                } else {
                    JOptionPane.showMessageDialog(null, "Course or Department not found!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (java.lang.NumberFormatException sda) {
            JOptionPane.showMessageDialog(null, "ID should be numbers only!", "Important Note", HEIGHT, warn);
        }
    }//GEN-LAST:event_jLabel20MouseClicked

