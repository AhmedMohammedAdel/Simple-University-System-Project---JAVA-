    private void studentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentMouseClicked
        String Message = "Key is incorrect !,Please enter correct key";
        studentkey = stu.getText();
        try {
            if (studentkey.equals("1")) {
                setVisible(false);
                s.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, Message, "Importanat Note", HEIGHT, key);
            }
        } catch (java.lang.NullPointerException as) {
            JOptionPane.showMessageDialog(null, "Key is Empty ! , please Enter Key and try again", "Importanat Note", HEIGHT, key);

        }
    }//GEN-LAST:event_studentMouseClicked

